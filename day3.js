// JavaScript Fundamentals Day 3
// Topic: Arithmetic Operators

// Addition (+)

let num1 = 10;
let num2 = 5;

let result = num1 + num2;
console.log(result);

// Output: 15
// Reason: 10 + 5 = 15


// Subtraction (-)

let a = 20;
let b = 8;

let answer = a - b;
console.log(answer);

// Output: 12
// Reason: 20 - 8 = 12


// Multiplication (*)

let x = 6;
let y = 4;

let multiply = x * y;
console.log(multiply);

// Output: 24
// Reason: 6 × 4 = 24


// Division (/)

let number1 = 18;
let number2 = 3;

let divide = number1 / number2;
console.log(divide);

// Output: 6
// Reason: 18 ÷ 3 = 6


// Modulus (%)

let value1 = 10;
let value2 = 3;

let remainder = value1 % value2;
console.log(remainder);

// Output: 1
// Reason: 10 ÷ 3 leaves remainder 1


// Exponent (**)

let base = 2;
let power = 3;

let exponent = base ** power;
console.log(exponent);

// Output: 8
// Reason: 2 × 2 × 2 = 8
